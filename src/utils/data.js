export const getOrderStatus = (status) => {
  switch (status) {
    case "cooking": {
      return <p className="text text_type_main-small">Готовится</p>;
    }
    case "pending": {
      return <p className="text text_type_main-small">Готовится</p>;
    }
    case "created": {
      return <p className="text text_type_main-small">Готовится</p>;
    }
    case "done": {
      return (
        <p className="text text_type_main-small" style={{ color: "#0cc" }}>
          Готов
        </p>
      );
    }
    case "cancelled": {
      return (
        <p className="text text_type_main-small" style={{ color: "#e52b1a" }}>
          Отменён
        </p>
      );
    }
    default: {
      return (
        <p className="text text_type_main-small" style={{ color: "#e52b1a" }}>
          Нет статуса
        </p>
      );
    }
  }
};

